import { Schema, model, models } from "mongoose";

const AdminSchema = new Schema({
  clerkId: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  photo: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
  },
  lastName: {
    type: String,
  }
},{timestamps: true});

const Admin = models?.Admin || model("Admin", AdminSchema);

export { Admin }